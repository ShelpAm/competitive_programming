using namespace std;
int a[21];
int k;
int n;
int b[21];
int y = 0;
int ans = 0;
bool isprime(int x)
{
    if (x == 1) {
        return false;
    }
    for (int i = 2; i * i <= x; i++) {
        if (x % i == 0) {
            return false;
        }
    }
    return true;
}
void dfs(int x, int sum)
{
    if (x > k) {
        if (isprime(sum)) {
            ans++;
        }
        return;
    }
    for (int i = y; i <= n; i++) {
        if (b[i] == 0) {
            b[i] = 1;
            int origin_y = y;
            y = i + 1;
            dfs(x + 1, sum + a[i]);
            y = origin_y;
            b[i] = 0;
        }
    }
}
int main()
{
    cin >> n >> k;
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
    }
    y = 1;
    dfs(1, 0);
    cout << ans;
}
